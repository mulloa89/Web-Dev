/* While Loops */

//Printing numbers from 1-5
var count = 1;

while (count < 6) {
  console.log("count is: " + count);
  count++;
}

/*  */

//Printing each character in a string

//string we're looping over:
var str = "hello";
//first character is at index 0
var count = 0;

while (count < str.length) {
  console.log(str[count]);
  count++;
}

